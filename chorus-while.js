//while loop syntax

var chorus = "Let's dance!";
var repeat =0;

while (repeat < 10) {
	if (repeat === 5) {
		console.log("*change key*");
	}
	console.log(chorus);
	repeat++;
}

console.log("Until the sun comes up!");

//For loop syntax

var chorus = "let's dance!";
for (var repeat = 0; repeat < 10; repeat++){
	console.log(chorus);
}
